// Define the function makeShirt that accepts a size and message as parameters
function makeShirt1(size, message) {
    // Print a sentence summarizing the size of the shirt and the message printed on it
    console.log("The shirt size is ".concat(size, " and the message printed on it is: \"").concat(message, "\"."));
}
// Call the function makeShirt with size and message parameters
makeShirt1("Large", "Hello, World!");
